void optionTypeChoice(std::string _optionType,std::string _valueType,std::string sUt, OptionType &T,Greeks &G,underlyingType &uT){
	if(_optionType == "call"){
		T = OptionType::CALL;
	}else if (_optionType == "put"){
		T = OptionType::PUT;
	}else if(_optionType == "cuo"){
		T = OptionType::CallUpOut;
	}else if(_optionType == "cdo"){
		T = OptionType::CallDownOut;
	}else if(_optionType == "cui"){
		T = OptionType::CallUpIn;
	}else if(_optionType == "cdi"){
		T = OptionType::CallDownIn;
	}else if(_optionType == "pdo"){
		T = OptionType::PutDownOut;
	}else if(_optionType == "pdi"){
		T = OptionType::PutDownIn;
	}else if(_optionType == "puo"){
		T = OptionType::PutUpOut;
	}else if(_optionType == "pui"){
		T = OptionType::PutUpIn;
	}
	else{
		THROW_XLW("Error optionType:call put cuo cui cdi ...");
	}

	if(_valueType == "p"){
		G = Greeks::PRICE;
	}
	else if(_valueType == "d"){
		G = Greeks::DELTA;
	}
	else if(_valueType == "v"){
		G = Greeks::VEGA;	
	}
	else if(_valueType == "g"){
		G = Greeks::GAMMA;
	}
	else if(_valueType == "t"){
		G = Greeks::THETA;		
	}
	else if(_valueType == "r"){
		G = Greeks::RHO;
	}
	else{
		THROW_XLW("Error _valueType:p d v g t r => price delta vega gamma theta rho");
	}


	if(sUt == "futures"){
		uT = underlyingType::Future;
	}
	else if(sUt == "stock"){
		uT = underlyingType::Stock;
	}
	else{
		uT = underlyingType::Future;
	}


}

