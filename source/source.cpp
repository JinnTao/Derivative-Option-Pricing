double TD_ewma_vol(const MyMatrix &logReturn,double lamuda = 0.94){
	int	closeRow = int(logReturn.rows());
	int closeClo = int(logReturn.columns());
	if(closeClo != 1){
		THROW_XLW("Only one Data need!");
	}
	if(closeRow < 2){
		THROW_XLW("Data not enough!");
	}

	MyArray Return(closeRow-1);
	MyArray VolSquare(closeRow-1);
	double sum_return = 0;
	double sum_lamada = 0;
	double sum_return_u = 0;
	double E_U;
	//ÊÕÒæÂÊÈ¨ÖØ·Ö²¼
	for(int i = 0;i <= closeRow- 1;i++){
		sum_return += std::pow(lamuda,i) *  logReturn[0][closeRow - 1 - i];
		sum_lamada += std::pow(lamuda,i);
	}
	E_U = sum_return / sum_lamada;


	for(int i = 0;i<= closeRow-1;i++){
		sum_return_u += std::pow(lamuda,i) * ( logReturn[0][closeRow - 1 - i] - E_U) * ( logReturn[0][closeRow - 1 - i] - E_U);
	}
	
	return sqrt(sum_return_u / sum_lamada);
	
}

